#include <stdio.h>
/**
 * addition - adds two ints
 * @i: first int
 * @j: second int
 * Return: sum of i and j
 */
int addition(int i, int j)
{
	return (i + j);
}

/**
 * subtract - subtracts two ints
 * @i: first int
 * @j: second int
 * Return: difference of int i and j
 */
int subtract(int i, int j)
{
	return (i - j);
}

/**
 * multiply - multiplys two ints
 * @i: first int
 * @j: second int
 * Return: product of i and j
 */
int multiply(int i, int j)
{
	return (i * j);
}

/**
 * division - divides two ints
 * @i: first int
 * @j: second int
 * Return: division result of i by j
 */
int division(int i, int j)
{
	return (i / j);
}

/**
 * modulo - returns the remainder of division of two ints
 * @i: first int
 * @j: second int
 * Return: the remainder of division of i by j
 */
int modulo(int i, int j)
{
	return (i % j);
}
